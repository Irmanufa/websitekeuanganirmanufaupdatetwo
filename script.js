function initCursor() {
  const cursorDot = document.querySelector(".cursor-dot");
  const cursorOutline = document.querySelector(".cursor-outline");

  if (!cursorDot || !cursorOutline) return;

  document.addEventListener("mousemove", (e) => {
    cursorDot.style.transform = `translate(${e.clientX - 4}px, ${e.clientY - 4}px)`;
    cursorOutline.style.transform = `translate(${e.clientX - 20}px, ${e.clientY - 20}px)`;
  });

  document.addEventListener("mousedown", () => {
    cursorOutline.style.transform += " scale(0.8)";
  });

  document.addEventListener("mouseup", () => {
    cursorOutline.style.transform = cursorOutline.style.transform.replace(
      " scale(0.8)",
      "",
    );
  });
}

function formatRupiah(amount) {
  return "Rp " + amount.toLocaleString("id-ID");
}

function getCurrentDateTime() {
  const now = new Date();
  return now.toISOString().slice(0, 19).replace("T", " ");
}

function getTodayDate() {
  return new Date().toISOString().slice(0, 10);
}

function getCurrentYearMonth() {
  const now = new Date();
  return `${now.getFullYear()}-${String(now.getMonth() + 1).padStart(2, "0")}`;
}
// ======================== DASHBOARD ========================
function totalIncome() {
  return transactions
    .filter((t) => t.type === "income")
    .reduce((sum, t) => sum + t.amount, 0);
}

function totalExpense() {
  return transactions
    .filter((t) => t.type === "expense")
    .reduce((sum, t) => sum + t.amount, 0);
}

function activePayingCount() {
  return new Set(payments.map((p) => p.memberCode)).size;
}

function getMostCompliant() {
  if (payments.length === 0) return { name: "-", count: 0 };
  const countMap = new Map();
  payments.forEach((p) =>
    countMap.set(p.memberCode, (countMap.get(p.memberCode) || 0) + 1),
  );
  let maxCode = null,
    maxCount = 0;
  for (let [code, cnt] of countMap) {
    if (cnt > maxCount) {
      maxCount = cnt;
      maxCode = code;
    }
  }
  const member = members.find((m) => m.code === maxCode);
  return { name: member ? member.name : "Unknown", count: maxCount };
}

function getMonthSummary() {
  const currentYearMonth = getCurrentYearMonth();
  const monthTransactions = transactions.filter((t) =>
    t.date.startsWith(currentYearMonth),
  );
  const monthIncome = monthTransactions
    .filter((t) => t.type === "income")
    .reduce((sum, t) => sum + t.amount, 0);
  const monthExpense = monthTransactions
    .filter((t) => t.type === "expense")
    .reduce((sum, t) => sum + t.amount, 0);
  const maxVal = Math.max(monthIncome, monthExpense, 1);
  return {
    income: monthIncome,
    expense: monthExpense,
    balance: monthIncome - monthExpense,
    incomePercent: (monthIncome / maxVal) * 100,
    expensePercent: (monthExpense / maxVal) * 100,
  };
}

function updateMonthSummary() {
  const summary = getMonthSummary();
  document.getElementById("monthIncome").innerText = formatRupiah(
    summary.income,
  );
  document.getElementById("monthExpense").innerText = formatRupiah(
    summary.expense,
  );
  document.getElementById("monthBalance").innerText = formatRupiah(
    summary.balance,
  );

  const incomeProgress = document.querySelector(".income-progress");
  const expenseProgress = document.querySelector(".expense-progress");
  if (incomeProgress) incomeProgress.style.width = `${summary.incomePercent}%`;
  if (expenseProgress)
    expenseProgress.style.width = `${summary.expensePercent}%`;
}

