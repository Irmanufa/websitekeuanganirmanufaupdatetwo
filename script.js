// ======================== RESET DATA (Harian & Semua) ========================
function updateResetStats() {
  const todayDate = getTodayDate();
  const todayIncomes = transactions.filter(
    (t) => t.type === "income" && t.date.startsWith(todayDate),
  );
  const todayExpenses = transactions.filter(
    (t) => t.type === "expense" && t.date.startsWith(todayDate),
  );
  const todayIncomeTotal = todayIncomes.reduce((sum, t) => sum + t.amount, 0);
  const todayExpenseTotal = todayExpenses.reduce((sum, t) => sum + t.amount, 0);
  const todayPayers = new Set(
    payments
      .filter((p) => p.date.startsWith(todayDate))
      .map((p) => p.memberCode),
  ).size;

  document.getElementById("todayTransCount").innerText =
    todayIncomes.length + todayExpenses.length;
  document.getElementById("todayIncomeTotal").innerText =
    formatRupiah(todayIncomeTotal);
  document.getElementById("todayExpenseTotal").innerText =
    formatRupiah(todayExpenseTotal);
  document.getElementById("todayPayersCount").innerText = todayPayers;
}

function setupDailyReset() {
  const confirmReset = document.getElementById("confirmReset");
  const resetDailyBtn = document.getElementById("resetDailyBtn");
  const confirmResetAll = document.getElementById("confirmResetAll");
  const resetAllBtn = document.getElementById("resetAllBtn");

  if (confirmReset) {
    confirmReset.addEventListener("change", () => {
      resetDailyBtn.disabled = !confirmReset.checked;
    });
  }
  if (resetDailyBtn) {
    resetDailyBtn.addEventListener("click", () => {
      const todayDate = getTodayDate();
      const todayIncomes = transactions.filter(
        (t) => t.type === "income" && t.date.startsWith(todayDate),
      );
      const todayExpenses = transactions.filter(
        (t) => t.type === "expense" && t.date.startsWith(todayDate),
      );
      const todayPayments = payments.filter((p) =>
        p.date.startsWith(todayDate),
      );

      if (
        todayIncomes.length === 0 &&
        todayExpenses.length === 0 &&
        todayPayments.length === 0
      ) {
        showToast("Tidak ada data kas hari ini yang perlu direset!", "info");
        return;
      }

      showConfirm(
        "Reset Data Hari Ini",
        `Anda akan menghapus:\n📥 ${todayIncomes.length} pemasukan\n📤 ${todayExpenses.length} pengeluaran\n👥 ${todayPayments.length} pembayaran\n\nYakin ingin melanjutkan?`,
        () => {
          transactions = transactions.filter(
            (t) => !t.date.startsWith(todayDate),
          );
          payments = payments.filter((p) => !p.date.startsWith(todayDate));
          saveAll();

          showToast(`✅ Berhasil menghapus data kas hari ini!`, "success");
          confirmReset.checked = false;
          resetDailyBtn.disabled = true;
          updateResetStats();
          updateDashboard();
          renderTransactions();
          renderMemberList();
        },
      );
    });
  }

  if (confirmResetAll) {
    confirmResetAll.addEventListener("change", () => {
      resetAllBtn.disabled = !confirmResetAll.checked;
    });
  }
  if (resetAllBtn) {
    resetAllBtn.addEventListener("click", () => {
      showConfirm(
        "Reset Semua Data",
        "⚠️ PERINGATAN: Semua data akan dihapus!\n\n- Semua transaksi pemasukan\n- Semua transaksi pengeluaran\n- Semua data pembayaran\n\nData TIDAK dapat dikembalikan!\n\nYakin ingin melanjutkan?",
        () => {
          resetAllData();
          showToast(
            "✅ Semua data berhasil dihapus! Memulai dari awal.",
            "success",
          );
          confirmResetAll.checked = false;
          resetAllBtn.disabled = true;
          updateResetStats();
          updateDashboard();
          renderTransactions();
          renderMemberList();
        },
      );
    });
  }
}

// ======================== NAVIGATION & CLOCK ========================
function setupNavigation() {
  const navBtns = document.querySelectorAll(".nav-btn");
  const mobileHamburger = document.getElementById("mobileHamburger");
  const sidebar = document.getElementById("sidebar");
  const overlay = document.getElementById("overlay");

  function closeSidebar() {
    sidebar.classList.remove("open");
    overlay.style.display = "none";
  }
  function openSidebar() {
    sidebar.classList.add("open");
    overlay.style.display = "block";
  }

  if (mobileHamburger) mobileHamburger.addEventListener("click", openSidebar);
  if (overlay) overlay.addEventListener("click", closeSidebar);

  navBtns.forEach((btn) => {
    btn.addEventListener("click", () => {
      const pageId = `page-${btn.dataset.page}`;
      document
        .querySelectorAll(".page")
        .forEach((p) => p.classList.remove("active"));
      document.getElementById(pageId).classList.add("active");
      navBtns.forEach((b) => b.classList.remove("active"));
      btn.classList.add("active");
      closeSidebar();

      if (btn.dataset.page === "payment") renderMemberList();
      if (btn.dataset.page === "members") renderMembersCrud();
      if (btn.dataset.page === "transactions") renderTransactions();
      if (btn.dataset.page === "dashboard") updateDashboard();
      if (btn.dataset.page === "dailyreset") updateResetStats();
    });
  });
}

function updateClock() {
  const clock = document.getElementById("liveClock");
  if (clock) {
    const update = () => {
      clock.innerText =
        new Date().toLocaleDateString("id-ID") +
        " | " +
        new Date().toLocaleTimeString("id-ID");
    };
    update();
    setInterval(update, 1000);
  }
}

