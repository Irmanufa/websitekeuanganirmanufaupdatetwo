// ======================== LOAD & SAVE ========================
function loadData() {
  const savedMembers = localStorage.getItem("irmanufa_members_v2");
  members = savedMembers
    ? JSON.parse(savedMembers)
    : JSON.parse(JSON.stringify(defaultMembers));

  const savedTransactions = localStorage.getItem("irmanufa_transactions_v2");
  transactions = savedTransactions ? JSON.parse(savedTransactions) : [];

  const savedPayments = localStorage.getItem("irmanufa_payments_v2");
  payments = savedPayments ? JSON.parse(savedPayments) : [];
}

function saveAll() {
  localStorage.setItem("irmanufa_members_v2", JSON.stringify(members));
  localStorage.setItem(
    "irmanufa_transactions_v2",
    JSON.stringify(transactions),
  );
  localStorage.setItem("irmanufa_payments_v2", JSON.stringify(payments));
}

function resetAllData() {
  members = JSON.parse(JSON.stringify(defaultMembers));
  transactions = [];
  payments = [];
  saveAll();
}

