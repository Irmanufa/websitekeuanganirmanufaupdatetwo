function processCustomPayment() {
  if (!selectedMemberForPayment) return;

  const customAmount = document.getElementById("customAmount").value;
  const paymentNote = document.getElementById("paymentNote").value;
  let amount = null;

  if (
    customAmount &&
    !isNaN(parseInt(customAmount)) &&
    parseInt(customAmount) > 0
  ) {
    amount = parseInt(customAmount);
  }

  if (!amount) {
    showToast(
      "Silakan pilih nominal atau masukkan nominal yang valid!",
      "error",
    );
    return;
  }

  const member = members.find((m) => m.code === selectedMemberForPayment);
  if (!member) return;

  const dateTime = getCurrentDateTime();
  const weeksCount = Math.floor(amount / 2000);
  const weeksText =
    weeksCount > 1
      ? ` (${weeksCount} minggu/tunggakan)`
      : weeksCount === 1
        ? " (1 minggu)"
        : "";
  const noteText = paymentNote ? ` - ${paymentNote}` : "";

  payments.push({
    memberCode: selectedMemberForPayment,
    amount,
    date: dateTime,
  });
  transactions.push({
    id: Date.now(),
    type: "income",
    amount,
    description: `Pembayaran kas dari ${member.name} sebesar ${formatRupiah(amount)}${weeksText}${noteText}`,
    date: dateTime,
  });

  saveAll();
  showToast(
    `${member.name} berhasil membayar ${formatRupiah(amount)}${weeksText}!`,
    "success",
  );
  closeCustomPaymentModal();
  renderMemberList();
  updateDashboard();
  renderTransactions();
}

