function showToast(message, type = "success") {
  const toastContainer = document.getElementById("toastNotification");
  if (!toastContainer) return;

  const toast = document.createElement("div");
  toast.className = `toast ${type}`;

  let icon = "";
  if (type === "success") icon = '<i class="fas fa-check-circle"></i>';
  else if (type === "error") icon = '<i class="fas fa-exclamation-circle"></i>';
  else icon = '<i class="fas fa-info-circle"></i>';

  toast.innerHTML = `${icon}<span>${message}</span>`;
  toastContainer.appendChild(toast);

  setTimeout(() => {
    toast.style.animation = "slideOutToast 0.3s ease forwards";
    setTimeout(() => toast.remove(), 300);
  }, 3000);
}

function showConfirm(title, message, onConfirm, onCancel = null) {
  const modal = document.getElementById("notificationModal");
  const titleEl = document.getElementById("notificationTitle");
  const messageEl = document.getElementById("notificationMessage");
  const iconEl = document.getElementById("notificationIcon");
  const confirmBtn = document.getElementById("notificationConfirm");
  const cancelBtn = document.getElementById("notificationCancel");

  titleEl.innerText = title;
  messageEl.innerText = message;
  iconEl.innerHTML = '<i class="fas fa-exclamation-triangle"></i>';
  iconEl.style.background = "rgba(220, 38, 38, 0.2)";
  iconEl.querySelector("i").style.color = "#ef4444";

  modal.classList.add("show");

  const handleConfirm = () => {
    modal.classList.remove("show");
    confirmBtn.removeEventListener("click", handleConfirm);
    cancelBtn.removeEventListener("click", handleCancel);
    if (onConfirm) onConfirm();
  };

  const handleCancel = () => {
    modal.classList.remove("show");
    confirmBtn.removeEventListener("click", handleConfirm);
    cancelBtn.removeEventListener("click", handleCancel);
    if (onCancel) onCancel();
  };

  confirmBtn.addEventListener("click", handleConfirm);
  cancelBtn.addEventListener("click", handleCancel);
}

